var a = 23;
var b = 4;

// soma
var sum = a + b;
console.log("O resultado da soma de "+ a + " + "   + b + " = "  + sum);
// subtração
var subt = a - b;
console.log("O resultado da subtração de "+ a + "- "   + b + " = "  + subt);
// multiplicação
var mult = a * b;
console.log("O resultado damultiplicação de "+ a + " x "   + b + " = "  + mult);
// divisão
var div = a / b;
console.log("O resultado da divisão de "+ a + " / "   + b + " = "  + div);
//modulo
var modulo = a % b;
console.log("O resultado do modulo de "+ a + " | "   + b + " = "  + modulo);


